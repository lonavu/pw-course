// Biến myName được khai báo là const nên ko thể gán lại giá trị
// Cách 1: Khai báo biến myName là let để cho phép gán lại giá trị
let myName = "Alex";
myName = "Nagi";
console.log(myName);

// Cách 2: Giữ nguyên khai báo biến là const, không gán lại giá trị
const myName1 = "Alex";
console.log(myName1);